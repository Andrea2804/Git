package it.beTalent.entities;

public class Calcolatrice {
	
	public static void somma(int num1, int num2) {
		System.out.println("La somma dei due numeri è: " + (num1+num2));
	}
	
	public static void sottrazione(int num1, int num2) {
		System.out.println("La sottrazione tra i due numeri è: " + (num1-num2));
	}
	
	public static void divisione(int num1, int num2) {
		System.out.println("La divisione tra i due numeri è: " + (num1/num2));
	}
	
	public static void moltiplicazione(int num1, int num2) {
		System.out.println("La moltiplicazione tra i due numeri è: " + (num1*num2));
	}

}
